    private void init() {
        try {
            initValidation();
            initRepository();
            initTable();
        } catch (Exception ex) {
            Logger.getLogger(EditArticlesPanel.class.getName()).log(Level.SEVERE, null, ex);
            MessageUtils.showErrorMessage("Unrecoverable error", "Cannot initiate the form");
            System.out.println("---------------");
            System.out.println(ex.getMessage());
            System.exit(1);
        }
    }

    private void initValidation() {
        validationFields = Arrays.asList(tfTitle, tfDuration, taDescription, tfPublishedDate, tfBeginDate, tfPicturePath);
        errorLabels = Arrays.asList(lbTitleError, lbDurationError, lbDescError, lbPubDateError, lbBeginDateError, lbIconError);
    }

    private void initRepository() throws Exception {
        repository = RepositoryFactory.getRepository();
    }

    private void initTable() throws Exception {
        tbMovies.setSelectionMode(ListSelectionModel.SINGLE_SELECTION);
        tbMovies.setAutoCreateRowSorter(true);
        tbMovies.setRowHeight(25);
        movieTableModel = new MovieTableModel(repository.getMovies());
        tbMovies.setModel(movieTableModel);

    }

    private void fillForm(Movie movie) {
        if (movie.getPosterURL() != null && Files.exists(Paths.get(movie.getPosterURL()))) {
            tfPicturePath.setText(movie.getPosterURL());
            setIcon(lbIcon, new File(movie.getPosterURL()));

        }
        tfTitle.setText(movie.getTitle());
        tfDuration.setText(Integer.toString(movie.getDuration()));
        taDescription.setText(movie.getDescripiton());
        tfPublishedDate.setText(movie.getPublishedDate().format(Movie.DATE_FORMATTER));
        tfBeginDate.setText(movie.getBeginDate());
    }

    private void setIcon(JLabel label, File file) {
        try {
            label.setIcon(IconUtils.createIcon(file.getPath(), label.getWidth(), label.getHeight()));
        } catch (IOException ex) {
            Logger.getLogger(EditArticlesPanel.class.getName()).log(Level.SEVERE, null, ex);
        }
    }

    private void clearForm() {
        validationFields.forEach(e -> e.setText(""));
        errorLabels.forEach(e -> e.setText(""));

        lbIcon.setIcon(new ImageIcon(getClass().getResource("/assets/no_image.png")));

        selectedMovie = null;
    }

    private boolean formValid() {
        boolean ok = true;

        for (int i = 0; i < validationFields.size(); i++) {
            ok &= !validationFields.get(i).getText().trim().isEmpty();
            errorLabels.get(i).setText(validationFields.get(i).getText().trim().isEmpty() ? "X" : "");
            String name = validationFields.get(i).getName();
            if ("Date".equals(name)) {
                try {
                    LocalDateTime.parse(validationFields.get(i).getText().trim(), Movie.DATE_FORMATTER);
                    errorLabels.get(i).setText("");
                } catch (Exception e) {
                    ok = false;
                    errorLabels.get(i).setText("X");
                }
            }

            if ("Duration".equals(name)) {
                try {
                    Integer.parseInt(validationFields.get(i).getText().trim());
                } catch (Exception e) {
                     ok = false;
                    errorLabels.get(i).setText("X");
                }
            }
        }

        return ok;
    }

